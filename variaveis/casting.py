# a função casting converte tipos de dados númericos ou texto em outros dados
# numericos ou textos

# int
x = int(2)
y = int(2.8)
z = int('2')

print("A variavel x eh do tipo: ", type(x))
print("A variavel y eh do tipo: ", type(y))
print("A variavel z eh do tipo: ", type(z))


# float
a = float(2.3)
b = float(2)
c = float("2.3")
d = float("2")

print("A variavel a eh do tipo: ", type(a))
print("A variavel b eh do tipo: ", type(b))
print("A variavel c eh do tipo: ", type(c))
print("A variavel d eh do tipo: ", type(d))

# String

t = str("s1")
r = str(2)
q = str(2.3)

print("A variavel t eh do tipo: ", type(t))
print("A variavel r eh do tipo: ", type(r))
print("A variavel q eh do tipo: ", type(q))
